DROP TABLE if Exists TIMETABLE;
CREATE TABLE `COURSE_OUTLINE`.`TIMETABLE` (
  `CourseID` VARCHAR(45) NOT NULL,
  `SectionNum` VARCHAR(4) NOT NULL,
  `Days` VARCHAR(45) NULL,
  `Time` VARCHAR(45) NULL,
  `Location` VARCHAR(45) NULL,
  PRIMARY KEY (`CourseID`,`SectionNum`),

  CONSTRAINT `TIMETABLE`
    FOREIGN KEY (`CourseID` , `SectionNum`)
    REFERENCES `COURSE_OUTLINE`.`SECTION` (`CourseID` , `SectionNumber`)
    ON DELETE NO ACTION
    ON UPDATE CASCADE);







